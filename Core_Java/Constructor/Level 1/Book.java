public class Book {
    private String title;
    private String author;
    private double price;
    public Book() {
        this.title = "laxmi kant";
        this.author = "ramesh";
        this.price = 250;
    }
    public Book(String title, String author, double price) {
        this.title = title;
        this.author = author;
        this.price = price;
    }
}